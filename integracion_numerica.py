import numpy as np
from scipy import integrate

def punto_medio(f, a, b, n):
    h = (b - a) / n
    x = np.linspace(a, b, n + 1)
    c = (x[:-1] + x[1:]) / 2
    return h * np.sum(f(c))

def trapecio(f, a, b, n):
    h = (b - a) / n
    x = np.linspace(a, b, n + 1)
    y = f(x)
    return h * (y[0]/2 + np.sum(y[1:-1]) + y[-1]/2)

def simpson(f, a, b, n):
    if n % 2 != 0:
        n += 1
    h = (b - a) / n
    x = np.linspace(a, b, n + 1)
    y = f(x)
    resultado = y[0] + y[-1]
    resultado += 4 * np.sum(y[1:-1:2])
    resultado += 2 * np.sum(y[2:-2:2])
    return (h / 3) * resultado

problemas = [
    ("Integral 1: ∫₂⁴ dx/ln(x)",      lambda x: 1/np.log(x),          2, 4, 26),
    ("Integral 2: ∫₁² ln(x)/(x+2)dx", lambda x: np.log(x)/(x+2),      1, 2, 40),
    ("Integral 3: ∫₀¹ dx/√(1+x³)",    lambda x: 1/np.sqrt(1+x**3),    0, 1, 68),
    ("Integral 4: ∫₀¹ e^{-√x}dx",     lambda x: np.exp(-np.sqrt(x)),  0, 1, 100),
]

print(f"\n{'='*75}")
print(f"{'COMPARACIÓN DE MÉTODOS DE INTEGRACIÓN NUMÉRICA':^75}")
print(f"{'='*75}")

for nombre, f, a, b, n in problemas:
    pm    = punto_medio(f, a, b, n)
    tr    = trapecio(f, a, b, n)
    sp    = simpson(f, a, b, n)
    exact, _ = integrate.quad(f, a, b)

    print(f"\n{nombre}  (n = {n})")
    print(f"  Valor de referencia : {exact:.10f}")
    print(f"  {'Método':<20} {'Aproximación':>15} {'Error abs':>12}")
    print(f"  {'-'*50}")
    print(f"  {'Punto Medio':<20} {pm:>15.10f} {abs(pm-exact):>12.2e}")
    print(f"  {'Trapecio':<20} {tr:>15.10f} {abs(tr-exact):>12.2e}")
    print(f"  {'Simpson 1/3':<20} {sp:>15.10f} {abs(sp-exact):>12.2e}")

print(f"\n{'='*75}")
























































































































































































































































































































































































































































































































